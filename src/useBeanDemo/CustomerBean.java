package useBeanDemo;

public class CustomerBean {

	
	private String name = "John";
	private String mail = "John@gmail.com";
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	
	
	
}
